// Fungsi untuk mengacak urutan dalam array (Algoritma Fisher-Yates)
function shuffleArray(array) {
      for (let i = array.length - 1; i > 0; i--) {
            const j = Math.floor(Math.random() * (i + 1));
            [array[i], array[j]] = [array[j], array[i]];
      }
}

function shuffleQuestions() {
      let globalQuestionNo = 1;

      sections.forEach(section => {
            const groups = {};
            const orderedTypes = [];

            section.questions.forEach(q => {
                  if (!groups[q.type]) {
                        groups[q.type] = [];
                        orderedTypes.push(q.type);
                  }
                  groups[q.type].push(q);
            });

            const shuffledQuestions = [];
            orderedTypes.forEach(type => {
                  shuffleArray(groups[type]);

                  groups[type].forEach(q => {
                        q.no = globalQuestionNo++;
                        shuffledQuestions.push(q);
                  });
            });
            section.questions = shuffledQuestions;
      });
}

function startExam() {
      const hero = document.querySelector('.hero');
      if (hero) hero.classList.add('hide');
      shuffleQuestions();
      answers = {};
      localStorage.removeItem('jlptN4Answers');

      currentSectionIndex = 0;
      lockedSections = new Set();
      localStorage.setItem('jlptN4Locked', JSON.stringify([...lockedSections]));
      loadSection(0, true);
}

function loadSection(index, freshTimer = false) {
      stopTimer();
      currentSectionIndex = index;
      const section = sections[index];
      topbar.classList.remove('hide');
      timeLeft = freshTimer ? section.duration : Number(sessionStorage.getItem('time_' + section.id) || section.duration);
      renderSection(section);
      startTimer(section.duration);
      window.scrollTo(0, 0);
}

function renderSection(section) {
      const locked = lockedSections.has(section.id);
      const html = `
    <div class="card">
      <div class="sectionTitle">
        <div>
          <p class="small">${section.note}</p>
        </div>
        <span class="badge">${Math.round(section.duration / 60)} menit</span>
      </div>
      ${section.questions.map(q => renderQuestion(q, locked)).join('')}
      <div class="btnRow">
        <button class="successBtn" onclick="submitCurrentSection()">Submit Bagian Ini</button>
        ${currentSectionIndex > 0 ? `<button class="ghost" onclick="loadSection(${currentSectionIndex - 1})">Kembali</button>` : ''}
        <button class="ghost" onclick="saveAnswers(); alert('Jawaban tersimpan di browser ini.')">Simpan Jawaban</button>
      </div>
    </div>`;
      screen.innerHTML = html;
      restoreAnswersToInputs();
      updateProgress();
}

function renderQuestion(q, locked) {
      const name = `q${q.no}`;
      const selected = answers[name];
      let listeningTools = '';
      if (q.type === 'Listening') {
            listeningTools = `
      <div class="btnRow" style="margin-top:8px;margin-bottom:8px;">
        <button class="secondary" type="button" onclick="speakJapanese(${q.no})">Putar Script</button>
        <button class="ghost" type="button" onclick="toggleScript(${q.no})">Tampilkan/Sembunyikan Script</button>
      </div>
      <div id="script-${q.no}" class="scriptBox hiddenScript jp">${escapeHtml(q.script)}</div>`;
      }
      const passage = q.passage ? `<div class="readingPassage jp">${escapeHtml(q.passage)}</div>` : '';
      const prompt = q.prompt ? `<div class="small">${q.prompt}</div>` : '';
      return `
    <section class="question" id="question-${q.no}">
      <div class="questionHeader">
        <div class="qNo">${q.no}</div>
        <div>
          <div class="badge">${q.type}</div>
          ${passage}
          <div class="qText jp">${escapeHtml(q.text || '')}</div>
          ${prompt}
        </div>
      </div>
      ${listeningTools}
      <div class="choices">
        ${q.choices.map((choice, i) => `
          <label class="choice jp">
            <input type="radio" name="${name}" value="${i + 1}" ${selected == i + 1 ? 'checked' : ''} ${locked ? 'disabled' : ''} onchange="recordAnswer(${q.no}, ${i + 1})" />
            <span>${i + 1}. ${escapeHtml(choice)}</span>
          </label>
        `).join('')}
      </div>
    </section>`;
}

function escapeHtml(str) {
      return String(str)
            .replaceAll('&', '&amp;')
            .replaceAll('<', '&lt;')
            .replaceAll('>', '&gt;')
            .replaceAll('"', '&quot;')
            .replaceAll("'", '&#039;');
}

function recordAnswer(no, value) {
      answers[`q${no}`] = value;
      saveAnswers(false);
      updateProgress();
}

function saveAnswers(show = false) {
      document.querySelectorAll('input[type="radio"]:checked').forEach(input => {
            answers[input.name] = Number(input.value);
      });
      localStorage.setItem('jlptN4Answers', JSON.stringify(answers));
      if (show) alert('Jawaban tersimpan.');
}

function restoreAnswersToInputs() {
      Object.entries(answers).forEach(([name, value]) => {
            const input = document.querySelector(`input[name="${name}"][value="${value}"]`);
            if (input) input.checked = true;
      });
}

function startTimer(totalDuration) {
      updateTimerDisplay(totalDuration);
      timerInterval = setInterval(() => {
            timeLeft--;
            const section = sections[currentSectionIndex];
            sessionStorage.setItem('time_' + section.id, String(timeLeft));
            updateTimerDisplay(totalDuration);
            if (timeLeft <= 0) {
                  stopTimer();
                  alert('Waktu habis. Bagian ini akan dikunci.');
                  submitCurrentSection(true);
            }
      }, 1000);
}

function stopTimer() {
      if (timerInterval) clearInterval(timerInterval);
      timerInterval = null;
}

function updateTimerDisplay(totalDuration) {
      const safe = Math.max(0, timeLeft);
      const m = Math.floor(safe / 60).toString().padStart(2, '0');
      const s = Math.floor(safe % 60).toString().padStart(2, '0');
      timerBox.textContent = `⏱ ${m}:${s}`;
      timerBox.classList.toggle('danger', safe <= 300);
      const pct = Math.max(0, Math.min(100, (safe / totalDuration) * 100));
      progressInner.style.width = pct + '%';
}

function updateProgress() {
      const all = sections.flatMap(s => s.questions);
      const answered = all.filter(q => answers[`q${q.no}`]).length;
      if (currentSectionIndex === -1) return;
      document.title = `(${answered}/${all.length}) Simulasi JLPT N4`;
}

function submitCurrentSection(auto = false) {
      if (currentSectionIndex < 0) return;
      saveAnswers(false);
      const section = sections[currentSectionIndex];
      if (!auto) {
            const unanswered = section.questions.filter(q => !answers[`q${q.no}`]).length;
            if (unanswered > 0 && !confirm(`Masih ada ${unanswered} soal kosong di bagian ini. Tetap submit?`)) return;
      }
      lockedSections.add(section.id);
      localStorage.setItem('jlptN4Locked', JSON.stringify([...lockedSections]));
      stopTimer();
      sessionStorage.removeItem('time_' + section.id);
      if (currentSectionIndex < sections.length - 1) {
            const next = currentSectionIndex + 1;
            if (confirm('Bagian ini selesai. Lanjut ke bagian berikutnya?')) loadSection(next, true);
            else renderSection(section);
      } else {
            showResult(false);
      }
}

function showResult(fromHero = false) {
      const hero = document.querySelector('.hero');
      if (hero) hero.classList.add('hide');
      stopTimer();
      topbar.classList.add('hide');
      saveAnswers(false);

      const all = sections.flatMap(s => s.questions);
      const correct = all.filter(q => Number(answers[`q${q.no}`]) === q.answer).length;
      const pct = Math.round((correct / all.length) * 100);
      const sectionScores = sections.map(s => {
            const c = s.questions.filter(q => Number(answers[`q${q.no}`]) === q.answer).length;
            return { title: s.title, correct: c, total: s.questions.length };
      });

      let status = 'Perlu ulang dasar N4 dulu';
      if (pct >= 90) status = 'Sangat aman untuk latihan N4';
      else if (pct >= 73) status = 'Cukup kuat, tinggal review titik lemah';
      else if (pct >= 53) status = 'Borderline, harus drill grammar + listening';

      screen.innerHTML = `
    <div class="card">
      <div class="sectionTitle">
        <div>
          <h2>Hasil Simulasi</h2>
          <p class="small">${fromHero ? 'Review/kunci dibuka dari menu awal.' : 'Simulasi selesai.'}</p>
        </div>
        <span class="badge">${status}</span>
      </div>
      <div class="resultGrid">
        <div class="scoreCard"><span class="small">Total Benar</span><strong>${correct}/${all.length}</strong></div>
        <div class="scoreCard"><span class="small">Persentase</span><strong>${pct}%</strong></div>
        ${sectionScores.map(s => `<div class="scoreCard"><span class="small">${s.title}</span><strong>${s.correct}/${s.total}</strong></div>`).join('')}
      </div>
      <div class="btnRow">
        <button onclick="startExam()">Ulang dari Awal</button>
        <button class="ghost" onclick="resetExam()">Reset Semua</button>
      </div>
    </div>
    <div class="card">
      <h2>Review Jawaban</h2>
      ${sections.map(section => `
        <h3 style="margin-top:18px">${section.title}</h3>
        ${section.questions.map(q => {
            const user = answers[`q${q.no}`];
            const ok = Number(user) === q.answer;
            return `<div class="reviewItem ${ok ? 'correct' : 'wrong'}">
            <strong>No. ${q.no}</strong> — ${ok ? 'Benar' : 'Salah / Kosong'}<br>
            <span class="jp">${escapeHtml(q.text || '')}</span><br>
            Jawaban kamu: <strong>${user ? user + '. ' + escapeHtml(q.choices[user - 1]) : '-'}</strong><br>
            Kunci: <strong>${q.answer}. ${escapeHtml(q.choices[q.answer - 1])}</strong><br>
            <span class="small">${escapeHtml(q.exp || '')}</span>
          </div>`;
      }).join('')}
      `).join('')}
    </div>`;
      window.scrollTo(0, 0);
}

function resetExam() {
      if (!confirm('Reset semua jawaban dan timer?')) return;
      answers = {};
      lockedSections = new Set();
      localStorage.removeItem('jlptN4Answers');
      localStorage.removeItem('jlptN4Locked');
      sessionStorage.clear();
      stopTimer();
      currentSectionIndex = -1;
      topbar.classList.add('hide');
      screen.innerHTML = '';

      const hero = document.querySelector('.hero');
      if (hero) hero.classList.remove('hide');

      document.title = 'Simulasi JLPT N4';
      alert('Sudah di-reset. Klik Mulai Simulasi untuk mulai lagi.');
}

function toggleScript(no) {
      const el = document.getElementById('script-' + no);
      if (el) el.classList.toggle('hiddenScript');
}

function speakJapanese(no) {
      const q = sections.flatMap(s => s.questions).find(x => x.no === no);
      if (!q || !q.script) return;
      if (!('speechSynthesis' in window)) {
            alert('Browser ini tidak mendukung text-to-speech. Pakai tombol tampilkan script.');
            return;
      }
      window.speechSynthesis.cancel();
      const utter = new SpeechSynthesisUtterance(q.script);
      utter.lang = 'ja-JP';
      utter.rate = 0.9;
      utter.pitch = 1;
      const voices = window.speechSynthesis.getVoices();
      const jaVoice = voices.find(v => v.lang && v.lang.toLowerCase().startsWith('ja'));
      if (jaVoice) utter.voice = jaVoice;
      window.speechSynthesis.speak(utter);
}

